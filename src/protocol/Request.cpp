#include <vector>
#include <stdexcept>
#include <algorithm>
#include "common/Utils.h"
#include "common/traceout.h"
#include "Request.h"

//---------------------------------------------------------------------------------------
Request::Request()
   : mSessionId(-1)
//---------------------------------------------------------------------------------------
{
   TRC_DEBUG_FUNC_ENTER(0U, "");
   TRC_DEBUG_FUNC_EXIT(0U);
}

//---------------------------------------------------------------------------------------
Request::Request(const std::string& rawMessage)
   : mSessionId(-1)
//---------------------------------------------------------------------------------------
{
   TRC_DEBUG_FUNC_ENTER(0U, "");
   TRC_DEBUG_FUNC_EXIT(0U);
}

//---------------------------------------------------------------------------------------
Request::Request(const Message& message)
   : mSessionId(-1)
//---------------------------------------------------------------------------------------
{
   TRC_DEBUG_FUNC_ENTER(0U, "");

   mRawMessage = message.getRawMessage();
   mHeader = message.getHeader();
   mBody = message.getBody();
   mbValid = message.isValid();

   TRC_DEBUG_FUNC_EXIT(0U);
}


//---------------------------------------------------------------------------------------
Request::~Request()
//---------------------------------------------------------------------------------------
{
   TRC_DEBUG_FUNC_ENTER(0U, "");
   TRC_DEBUG_FUNC_EXIT(0U);
}

//---------------------------------------------------------------------------------------
Request::operator bool()
//---------------------------------------------------------------------------------------
{
    return isValid();
}

//---------------------------------------------------------------------------------------
void Request::setSessionId(int sessionId)
//---------------------------------------------------------------------------------------
{
    mSessionId = sessionId;
}

//---------------------------------------------------------------------------------------
const int Request::getSessionId() const
//---------------------------------------------------------------------------------------
{
    return mSessionId;
}
